function kad_all_ksp_in_array(){
    if (class_exists('KadenceSliderPro_Output')) {
        global $wpdb;
        $sliders = $wpdb->get_results('SELECT * FROM ' . $wpdb->prefix . 'ksp_sliders');
        if(!empty($sliders)) {
            $name  = array();
            $id     = array();
            foreach($sliders as $slider) {
                $name[] = $slider->name;
                $id[]   = $slider->id;
            }
            if(!empty($id)) {
                $result = array_combine($id, $name);
            } else { 
                $result = ''; 
            }
            return $result;
        }
    }
